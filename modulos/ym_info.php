            <h2>Para que mujeres de todo el mundo puedan contar su historia a trxavés de su estilo.</h2>
            <p>
                Pandora comenzó a diseñar sus queridos charms en el año 2000. Cada charm tiene un significado, a veces muchos significados, uno el que le concede su diseñador y el resto el que le concede la persona que lo lleva y lo cuida. Tanto si se trata de una celebración de color o de un patrón, como de un homenaje a un país, una ocasión, una actividad o, lo más importante, una persona, cada charm se ha diseñado para contar la historia de la persona que lo lleva además de mostrar su estilo personal. Nuestros charms se llevan con amor en pulseras y collares; creados especialmente para ser llevados de manera única por cada persona que se los pone.
            </p>
            <h3>1982-1999: LOS PRIMEROS AÑOS</h3>
           
            <p>
            Todo comienza hace ya 30 años. En 1982, Per Enevoldsen y su mujer fundan en Nørrebro (Copenhague) la joyería que más tarde se convertirá en PANDORA. El matrimonio viaja a menudo a Tailandia, donde selecciona joyas para importar. Sin embargo, debido al incremento de la demanda de estas piezas exclusivas, la empresa comienza a centrar su actividad en la venta al por mayor.
            <br>
            En 1987, y tras varios años de gran éxito como mayoristas, deciden abandonar definitivamente su actividad minorista y trasladar la empresa a locales de mayores dimensiones. Dos años de exitosa actividad mayorista en exclusiva animan al matrimonio a fundar su propia fábrica en Tailandia con el fin de poder participar activamente en el proceso creativo de sus joyas.
            </p>

            <h3>2000-2009: CONSOLIDANDO LA PRESENCIA INTERNACIONAL</h3>
            La pulsera PANDORA se lanza en el año 2000 en Dinamarca. Muy pronto, los consumidores comienzan a admirar y a desear ese nuevo concepto de joyería. PANDORA crece debido a la demanda del mercado, no sólo el danés, sino el mundial..

            <p>
                Con el fin de ampliar su capacidad de producción, en 2005 PANDORA decide abrir la primera fábrica, situada en Bangkok, donde expertos empleados elaboran de forma artesanal cada pieza de joyería, respetando los valores de calidad, tradición y tecnología moderna. La fábrica cumple con los estándares de eficiencia, de lo cual PANDORA está tremendamente orgullosa. La producción de PANDORA obtiene el certificado de Calidad ISO en 2008 (ISO 9001:2008 certification).
                <br>
                En 2009 la fábrica suministra más de 3 millones de piezas de joyería cada mes.
            </p>

            <h3>ACTUALMENTE</h3>
            <p>
                Hoy en día la firma PANDORA es conocida, a nivel mundial, por crear un nuevo concepto de joyería, realizada de forma artesanal utilizando los mejores materiales naturales y siempre cuidando al mínimo cada detalle. Oro y plata de ley, piedras preciosas y semipreciosas, cordones de algodón o de cuero natural, que se combinan en diseños exclusivos, elegantes y femeninos.
                <br>
                PANDORA opera y dirige un modelo de empresa integrado que apuesta por el sistema empresarial in-house y distribuye de forma global y directa a la mayoría de mercados. Las joyas se venden en más de 100 países de los 6 continentes. La firma de joyería PANDORA cuenta con, aproximadamente, 1.600 piezas de joyería que se venden en más de 7.700 puntos de venta, incluyendo las más de 2.600 boutiques propias de imagen.
                <br>
                La misión de PANDORA – hoy y siempre – es ofrecer a todas las mujeres del mundo un maravilloso universo de joyería, cuyas piezas se elaboran de forma artesanal, utilizando materiales de primera calidad y a un precio asequible. Las joyas PANDORA son genuinas, contemporáneas y femeninas e inspiran a las mujeres a expresar su individualidad.
                <br>
                Cada mujer posee una personalidad única que merece ser valorada y contada. Historias reales y personales creadas por instantes o momentos especiales. En PANDORA queremos celebrar cada uno de esos momentos vividos y hacerlos inolvidables. La vida tiene momentos Inolvidables…
            </p>
